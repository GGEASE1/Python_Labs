# Считываем сторону квадрата
a = int(input())

# Периметр: сумма длин всех сторон
perim = a * 4
# Площадь: произведение сторон
sq_area = a * a
# Диагональ: по теореме Пифагора (a * корень из 2)
diag = a * (2 ** 0.5)

# Форматированный вывод с точностью 2 знака
print(f"{perim:.2f}, {sq_area:.2f}, {diag:.2f}")