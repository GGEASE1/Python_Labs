# Получаем ввод в формате "число, число"
inp = input()

# Переменная для хранения позиции разделителя
sep_index = 0
# Проходим по строке, чтобы найти запятую
for i in range(len(inp)):
    if inp[i] == ',':
        sep_index = i
        break

# Срезаем строку до запятой и преобразуем в число
dividend = int(inp[:sep_index])
# Срезаем строку после запятой и пробела (+2), преобразуем в делитель
divisor = int(inp[sep_index+2:])

# Вычисляем и выводим остаток
print(dividend % divisor)